package Juego;

/**
 * La interfaz Sensor define un método para actualizar el tablero del juego.
 */
public interface Sensor {

    /**
     * Actualiza el tablero del juego según algún evento o cambio en el juego.
     * Este método se implementa para reflejar cualquier cambio en el estado del juego en el tablero.
     */
    void actualizarTablero();
}
